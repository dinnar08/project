    private void tcariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tcariKeyPressed
        // TODO add your handling code here:

        Object header[]={"KODE GALON","NAMA GALON","JENIS GALON","SATUAN"};
        DefaultTableModel data=new DefaultTableModel(null,header);
        tabelGalon.setModel(data);

        setKoneksi();
        String sql="Select * from data_galon where Gkode like '%" + tcari.getText() + "%'"
        +"or Gnama like '%" + tcari.getText()+"%'"
        +"or Gjenis like '%" + tcari.getText()+"%'";

        try {
            ResultSet rs=stm.executeQuery(sql);
            while (rs.next())
            {
                String kolom1=rs.getString("Gkode");
                String kolom2=rs.getString("Gnam");
                String kolom3=rs.getString("Gjenis");
                String kolom4=rs.getString("Gsatuan");

                String kolom[]={kolom1,kolom2,kolom3,kolom4};
                data.addRow(kolom);
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_tcariKeyPressed

