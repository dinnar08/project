    public Connection setKoneksi(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn=DriverManager.getConnection("jdbc:mysql://localhost/daur_galon","root","");
            stm=conn.createStatement();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Koneksi Gagal :" +e);
        }
       return conn; 
    }
    
    public void tabelGalon(){
        Object header[]={"KODE GALON","NAMA GALON","JENIS GALON","SATUAN"};
        DefaultTableModel data=new DefaultTableModel(null,header);
        tabelGalon.setModel(data);
        setKoneksi();
        String sql="select*from data_galon";
        try {
            ResultSet rs=stm.executeQuery(sql);
            while (rs.next())
            {
                String kolom1=rs.getString("Gkode");
                String kolom2=rs.getString("Gnama");
                String kolom3=rs.getString("Gjenis");
                String kolom4=rs.getString("Gsatuan");
                
                String kolom[]={kolom1,kolom2,kolom3,kolom4};
                data.addRow(kolom);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
        }
    }
    

