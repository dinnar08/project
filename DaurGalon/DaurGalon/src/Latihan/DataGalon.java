    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        // TODO add your handling code here:
        String sql = "insert into data_galon values (?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, kdG.getText());
            stat.setString(2, nmG.getText());;
            stat.setString(3, satuanG.getSelectedItem().toString());
            stat.setString(4, satuanG.getSelectedItem().toString());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data akan disimpan");
            kosong();
            kdG.requestFocus();
            tabelGalon();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal disimpan"+e);
            tabelGalon();
        }
    }//GEN-LAST:event_bSaveActionPerformed

