    private void bEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditActionPerformed
        // TODO add your handling code here:
        
        try {
            String sql = "update data_pembeli set IDP=?, namaP=?, JKP=?, TGLP=?, AlamatP=?, UkurP=?, JenisP=? where IDP =?";
            PreparedStatement stat = conn.prepareStatement(sql);    
            stat.setString(1, tid.getText());
            stat.setString(2, tnama.getText());
            
            String jkel="";
            if(rjk1.isSelected()) jkel="Laki-laki";
            else jkel="Perempuan";
            stat.setString(3, jkel);
            
            SimpleDateFormat fm = new SimpleDateFormat("yyyy-MM-dd");   
            String tgl = String.valueOf(fm.format(dTglLahir.getDate()));
            stat.setString(4, tgl);
            stat.setString(5, talamat.getText());
            stat.setString(6, Pukur.getText());
            stat.setString(7, Pjenis.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            tid.requestFocus();
            tabelPembeli();

            
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Data Gagal Diubah" +e);
        }
        
    }//GEN-LAST:event_bEditActionPerformed

