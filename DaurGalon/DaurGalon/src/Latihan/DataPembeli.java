    public void tabelPembeli(){
        Object header[]={"NO ID","NAMA PELANGGAN","JENIS KELAMIN","TANGGAL BELI","ALAMAT","UKURAN GALON","JENIS GALON"};
        DefaultTableModel data=new DefaultTableModel(null,header);
        tabelPembeli.setModel(data);
        setKoneksi();
        String sql="select*from data_pembeli";
        try {
            ResultSet rs=stm.executeQuery(sql);
            while (rs.next())
            {
                String kolom1=rs.getString("IDP");
                String kolom2=rs.getString("NamaP");
                String kolom3=rs.getString("JKP");
                String kolom4=rs.getString("TGLP");
                String kolom5=rs.getString("AlamatP");
                String kolom6=rs.getString("UkurP");
                String kolom7=rs.getString("JenisP");
                String kolom[]={kolom1,kolom2,kolom3,kolom4,kolom5,kolom6,kolom7};
                data.addRow(kolom);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
        }
    }

